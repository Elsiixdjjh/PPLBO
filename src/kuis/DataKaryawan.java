    private void tabelKaryawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelKaryawanMouseClicked
        // TODO add your handling code here:
        int baris = tabelKaryawan.rowAtPoint(evt.getPoint());
        String id = tabelKaryawan.getValueAt (baris, 1).toString();
        txtId.setText(id);
        
        String nama = tabelKaryawan.getValueAt (baris, 2).toString();
        txtNama.setText(nama);
        
        String jab = tabelKaryawan.getValueAt(baris, 3).toString();
        comboJabatan.setSelectedItem(jab);
        
        String tahun  = tabelKaryawan.getValueAt(baris, 4).toString();
        txtTahun.setText(tahun);
        
        String alamat = tabelKaryawan.getValueAt(baris, 5).toString();
        txtAlamat.setText(alamat);
        
        String surel = tabelKaryawan.getValueAt(baris, 6).toString();
        txtSurel.setText(surel);
        
        String telepon = tabelKaryawan.getValueAt(baris, 7).toString();
        txtTelepon.setText(telepon);

    }//GEN-LAST:event_tabelKaryawanMouseClicked

