    private void printData(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("ID Karyawan");
        model.addColumn("Nama");
        model.addColumn("Jabatan");
        model.addColumn("Tahun Masuk");
        model.addColumn("Alamat");
        model.addColumn("Surel");
        model.addColumn("No. Telepon");
        
        try{
            int no = 1;
            String sql = "SELECT * FROM dataKaryawan";
            java.sql.Connection conn = (Connection)Configuration.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{no++, res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getString(7)});
            }
            tabelKaryawan.setModel(model);
        }catch(SQLException e){
            System.out.println("Error" + e.getMessage());
        }
    }
    
